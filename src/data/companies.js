export function findCompanyByBrand(brandName) {
  if (!brandName) return null;
  const normalized = brandName.toLowerCase().trim();

  return companies.find((company) => {
    if (company.name.toLowerCase() === normalized) return true;
    if (company.name.toLowerCase().includes(normalized)) return true;
    if (normalized.includes(company.name.toLowerCase())) return true;
    return company.aliases.some(
      (alias) =>
        alias.toLowerCase() === normalized ||
        alias.toLowerCase().includes(normalized) ||
        normalized.includes(alias.toLowerCase())
    );
  });
}

export function findCompanyByProductType(productType, category) {
  const normalized = productType.toLowerCase().trim();

  // Try exact key match first
  for (const [key, ids] of Object.entries(productTypeDefaults)) {
    if (normalized.includes(key) || key.includes(normalized)) {
      const company = companies.find((c) => c.id === ids[0]);
      if (company) return company;
    }
  }

  // Fallback: first company in the right category
  return companies.find((c) => c.category === category) || companies[0];
}

